class Rect{
	int length,breadth;
	Rect(){
		Scanner obj=new Scanner(System.in);
		System.out.println("Enter length:");
		length=obj.nextInt();
		System.out.println("Enter Bredth:");
		breadth=obj.nextInt();
		
	}
	int area(){
		
		return length*breadth;
	}
	int peri(){
		
		return length+breadth;
	}
	void dis(){
		System.out.println("\n\n\n***Rectangle***\nArea:"+area()+"\nPerimeter:"+peri());
		
	}
	
}